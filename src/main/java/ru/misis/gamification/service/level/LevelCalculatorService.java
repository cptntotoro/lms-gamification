package ru.misis.gamification.service.level;

/**
 * Сервис расчета уровня пользователя на основе накопленных очков
 * <p>
 * Сервис является центральным компонентом системы геймификации, отвечающим за преобразование
 * общего количества очков ({@code totalPoints}) в уровень пользователя и расчёт количества
 * очков, необходимых для достижения следующего уровня.
 * </p>
 * <p>
 * При неизвестной или некорректной формуле используется fallback-режим: линейный расчёт уровня
 * с шагом в 1000 очков.
 * </p>
 */
public interface LevelCalculatorService {

    /**
     * Рассчитывает текущий уровень пользователя на основе накопленных очков
     * <p>
     * Выбор формулы осуществляется по значению свойства {@code formula}.
     * При неизвестной формуле используется fallback-режим.
     * </p>
     *
     * @param totalPoints Общее количество накопленных очков
     * @return Текущий уровень пользователя (всегда ≥ 1)
     */
    int calculateLevel(int totalPoints);

    /**
     * Рассчитывает количество очков, необходимых для достижения следующего уровня
     * <p>
     * Значение зависит от текущего уровня и выбранной формулы
     * </p>
     *
     * @param currentLevel Текущий уровень пользователя (должен быть ≥ 1)
     * @return Количество очков до следующего уровня (всегда > 0)
     */
    long pointsToNextLevel(int currentLevel);
}
